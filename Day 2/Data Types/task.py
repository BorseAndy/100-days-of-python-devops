#Subscipting
print("Hello"[4])   #Printing the character based on the position in the brackets
print("Hello"[-1])  #Printing the last character by counting negative

#String
print("123"+"345")  #Concatonating strings

#Integer =  Whole number
print(123 +345) # sum of the two numbers

#Large Integers
print(123_456_789)  # Underscores helps us to separate large numbers, python automatically deletes/ignores it

# Float = Floating point number
print(3.14159)

# Boolean
print(True)
print(False)